def choose_grid(nr):
    if nr>3:
        return nr // 3 + 1 if (nr % 3 != 0) else nr // 3 , 3
    return nr // 3 + 1, nr

def line_chart(ax: plt.Axes, series: pd.Series, title: str, xlabel: str, ylabel: str, percentage=False):
    ax.set_title(title)
    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    if percentage:
        ax.set_ylim(0.0, 1.0)
    ax.plot(series)


